import turtle
def rectangle(color):
	t.begin_fill()
	t.fillcolor(color)
	for i in range(2):
		t.fd(400)
		t.rt(90)
		t.fd(100)
		t.rt(90)
	t.end_fill()

t=turtle.Turtle()
s=turtle.Screen()
s.bgcolor("black")
t.up()
t.pensize(30)
t.color("brown")
t.goto(0,-300)
t.down()
t.goto(0,400)
t.pensize(0)
t.color("orange")
rectangle("orange")
t.goto(0,300)
t.color("white")
rectangle("white")
t.fd(200)
t.pensize(4)
t.color("blue")
t.circle(-50)
t.setheading(270)
t.fd(50)
t.setheading(0)
for i in range(24):
	t.fd(45)
	t.bk(45)
	t.left(15)
t.setheading(90)
t.forward(50)
t.setheading(0)
t.pensize(0)
t.color("white")
t.fd(200)
t.rt(90)
t.fd(100)
t.rt(90)
t.fd(400)
t.rt(90)
t.fd(100)
t.rt(90)

t.goto(0,200)
t.color("green")
rectangle("green")
